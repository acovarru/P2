    <p style="color: black">What is xkcd password?</p>
<font size="4" color="gray">XKCD password is based on a webcomic created by Randall Munroe, it illustrates a password generated by random words.
This approach combines random words from a dictionary generating a password that has more entropy than usual passwords.
This is due to the fact that a dictionary word has an entropy of around 65000. Then if we add symbols and numbers we add more complexity to password
 and more difficult for cracking software to guess.<br>  </font>
 <br>
 <p style="color: black">Why to use an XKCD password generator?</p>
 
 <font size="4" color="gray">
 <ul>
     <li>Is easier to remember a sequence of random words than random letters 
     <li>Multiple words add complexity to password
     <li> Multiple symbols add complexity to password
     <li> Multiple numbers add complexity to password
 </ul>
 
  <br>
