<p style="color: black">Description of application </p>

<font size="4" color="gray">The application generates a random password based on the options provided above. User can select from:
<ul>
<li>Number of words to be included on password
<li>Include Numbers
<li>Include Symbols
<li>Number of symbols to be included on password
<li>Include an Uppercase for first letter
<li>Include Hyphen to separate words
</ul>
The process to generate the password is simple and user intuitive: user enables checkbox for selection to be included on password , inputs the number of words to be included on password and if symbol checkbox is selected user has the option to input the number of symbols to be included on password.
Once selections are made user clicks on generate!</font>


<p style="color: black">What if I only want words in my password? </p>

<font size="4" color="gray">Just input the number of words desired on your password and hit generate!</font>

<p style="color: black">What if want my password to begin with Uppercase? </p>

<font size="4" color="gray">Just input the number of words desired on your password, select Add Uppercase and hit generate!</font>

<p style="color: black">What if I want words and hyphen in my password? </p>

<font size="4" color="gray">Just input the number of words desired on your password, select Add Hyphen checkbox and hit generate!</font>

<p style="color: black">What if I want words and a random number in my password? </p>

<font size="4" color="gray">Just input the number of words desired on your password, select Add number checkbox and hit generate!</font>

<p style="color: black">What if I want words and a random symbols in my password? </p>

<font size="4" color="gray">Just input the number of words desired on your password, select Add symbol checkbox, input the number of symbols desired and hit generate!</font>

<p style="color: black">What if I want more complexity in my password? </p>

<font size="4" color="gray">Just input the number of words desired on your password, select Add symbol checkbox, select Add number checkbox, select Add Uppercase checkbox, input the number of symbols desired and hit generate!
